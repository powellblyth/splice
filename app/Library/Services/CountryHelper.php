    public static function getCountryCode(string $country): ?string {
        $resultCountry = null;
        if (!in_array(strToUpper($country), self::$countryCodes)) {
            // Try to find elsewhere
            if (array_key_exists(strToLower($country), self::$countryCodes)) {
                $resultCountry = self::$countryCodes[strToLower($country)];
            }
        } else {
            $resultCountry = strToUpper($country);
        }
        return strToUpper($resultCountry);

    }

